public class Calculator implements Icalc {


    public double add(double num, double add) {
        return num+add;
    }

    public double sub(double num, double sub) {
        return num-sub;
    }

    public double mult(double num, double mult) {
        return num*mult;
    }

    public int div(int num, int div) {
        return num/div;
    }
}
